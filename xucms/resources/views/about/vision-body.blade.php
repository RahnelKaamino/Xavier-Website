        <h2 class="text-font2">
            Mission
        </h2>

        <p class="text-font3">
            Xavier University (Ateneo de Cagayan) is a Filipino, Catholic and Jesuit educational community dedicated to the integral development of the person for the needs of Mindanao, the Philippines and Asia-Pacific. <br><br>


            As a University, Xavier engages in the authentic search for the truth through teaching, formation, research, and social outreach; it is dedicated to the renewal, discovery, safeguarding and communication of knowledge and human values; and it trains men and women to think rigorously, so as to act rightly and serve humanity justly. <br><br>
            As a Filipino University, Xavier is devoted to the appreciation, preservation and enrichment of the Filipino culture and heritage; to the sustainable development of the nation; and to the pursuit of the common good. <br><br>
            
            As a Catholic Univerity, Xavier is commited to the proclamation of the joy of the Gospel; its commitment is rooted in a deep personal friendship with Jesus Christ manifested by loyalty to the Church characterized by a preferential option for the poor; it shares in the privileged task of fostering the interdisciplinary and integrated encounter between faith, reason, and the sciences. <br><br>
            
            As a Jesuit University, Xavier participates in the Jesuit mission if reconciliation with God, with others and with creation; it seeks to serve the faith, promote justice, dialogue with culture and religions, and protect the environment; it upholds the Ignatian values of magis, cura personalis, and finding-God-in-all-things. <br><br>
            
            In sum, Xavier University forms men and women of competence, conscience and commitment in service of the Church, the global community and the Filipino people. <br>
        </p>
