        <h2 id="general" class="text-font2">Ideal Profile of An Atenean</h2>
        <div class="content" id="content">
            <br>
            <h4 class="text-font8">Academically Excellent</h4>
            <p class="text-font3">
                A Xavier Ateneo graduate possesses a high level of 
                competence in his/her field of specialization allowing 
                him/her to think and act critically and creatively, 
                and conduct research for the advancement of knowledge 
                in his/her discipline. 
            </p>
            <br>

            <h4 class="text-font8">Hollistically Formed</h4>
            <p class="text-font3">
                A Xavier Ateneo graduate does what is right in 
                the context of his/her religious faith and
                convictions, respects diversity among peoples, 
                serves as good stewards of creation,
                while upholding the Ignatian values of magis, 
                cura personalis and finding-God-in-all-things.
            </p>
            <br>

            <h4 class="text-font8">Socially Engaged</h4>
            <p class="text-font3">
                A Xavier Ateneo graduate possesses a deep sense of 
                leadership and service allowing
                him/her to engage actively as an agent of change 
                to achieve sustainable development of
                the people in Mindanao, the Philippines and the 
                global community. 
            </p>
            <br>

            <h4 class="text-font8">Globally Competitive</h4>
            <p class="text-font3">
                A Xavier Ateneo graduate is a life-long learner 
                equipped with the competencies that
                enable him/her to operate professionally, 
                confidently and comfortably across borders,
                cultures and languages, thereby transforming 
                him/her into a global citizen committed
                to shaping a just and peaceful world.
            </p>
