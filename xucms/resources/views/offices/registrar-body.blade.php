             Diploma/Authenticated Copy of Diploma<br>
             Course Description<br><br>

             Please note that:<br><br>
             No academic records will be released to a representative without a written authority from the student he/she represents and IDs of
             both student and the claimant.<br>
             The request will not be processed if the student has incomplete academic credentials (eg Form 138, Form 137-A, Transfer Credential,
             Official Transcript of Records, etc) <br>
             XU graduates and former students upon their first request of documents shall need clearance from the following offices: Office of
             Student Affairs, Book Center, University Libraries, Guidance and Counseling, and Finance Office. Currently enrolled students will
             need clearance only from the Finance Office.<br><br>

             Transfer Credential is released only once.<br>
             An Affidavit of Loss is required for re-issuance of diploma.<br>

