            up to date. The Office assists in the enforcement of Commission on Higher Education (CHED) and school regulations regarding the
            admission, enrolment, student load, subject sequence, crediting and equivalence, promotion, graduation, transfer, suspension and
            dismissal of a student in coordination with the proper college/school. The Office  prepares the academic calendar and consolidates
             the class and examination schedules; prepares and submits  pertinent reports to the CHED,the Professional Regulations Commission
             (PRC) and the Civil Service Commission (CSC) <br> <br>

             Among the documents/certifications which students may request from the Registrarâ€™s Office are the following:
             <br>(For corresponding fees, see Guidelines for Online Request.)<br><br>
