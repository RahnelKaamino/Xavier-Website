            <h4 class="text-font3">SOCIAL DEVELOPMENT CLUSTER</h4>
            <p>

                Forming men and women for and with others, the Xavier Ateneo Social Development cluster
                engages communities and institutions in working together towards peace and justice for
                the inclusive and sustainable development of Mindanao, Philippines and the global community.<br><br>

                The Social Development cluster is mandated to engage partner communities and institutions to enable
                Xavier Ateneo students, faculty and staff to utilize acquired knowledge and address development concerns
                and issues. This mandate is translated by the cluster, drawing guidance from the University’s mission-vision-goals,
                the themes of the Jesuit Province Roadmap and the Sustainable Development Goals of the United Nations.<br><br>

                Translating these goals to action, the cluster engages in three streams: Service Learning, Student Social Involvement
                & Advocacy Program, and Institutional Societal Engagement.<br><br>
                </p>
                <h5 class="text-font3">Social Development Units</h5>
                <h6 class="text-font3">I. SERVICE LEARNING PROGRAM</h6>
            <p>

                Service Learning Program is a curriculum-based and disciplinal student engagement program for social development.
                Students in their junior and senior years engage communities to apply their disciplinal foundation, knowledge and
                skill sets in crafting solutions to help address community concerns and problems.<br><br>

                The program facilitates three-pronged objectives:<br><br>
                1. Satisfy students’ curricular requirement<br>
                2. Contribute to community and social development<br>
                3. Enhance students’ social formation in the process of engaging marginal
                communities and reflecting on their experiences<br><br>


            </p>
            <h6 class="text-font3">II. STUDENT SOCIAL INVOLVEMENT AND ADVOCACY PROGRAM</h6>
            <p>
                The Student Social Involvement and Advocacy Program engages tertiary students at-large for involvement in social
                issues and concerns through exposures and immersions in communities and in social development work, mobilizations,
                volunteer work in SD cluster units’ initiatives and engagements as facilitators and through advocacy work. At the
                culmination of each engage,ent, the students undergo a processing session to dig through the experience and bring
                out the depth in their understanding and grasp of social ills that could lead to deeper commitment for and living
                out their faith that does social justice.<br><br>



            </p>
            <h6 class="text-font3">III. INSTITUTIONAL SOCIETAL ENGAGEMENT</h6>
            <p>
                Institutional Societal Engagement harnesses and maximizes the discipline-based expertise and skills of faculty,
                formators and staff engaging societal concerns and issues primarily through the college-based Social Development
                Centers. It provides oversight for harmonization and incubation of inter- and trans-disciplinary SD Center
                engagements and projects<br><br>

            </p>
            <h5 class="text-font3">Social Development Centers</h5>
            <h6 class="text-font3">I. MARINE MCKEOUGH CENTER</h6>
            <p>
                The Marine Mckeough Center is the research and social development arm of the University in the field of marine
                science. It also does research study on ecosystems adjacent to the coasts and bay. Its vision is for Northern
                Mindanao to attain sustainable management of its coastal and marine resources. The center is dedicated to the
                sustainable development of the coastal community and environment and that it will be a center of excellence for
                Marine Science research in the region.<br><br>

            </p>
            <h6 class="text-font3">II. CENTER FOR GLOBAL HEALTH</h6>
            <p>
                The Center for Global Health is a holistic science-based interdisciplinary center under the Jose P Rizal School
                of Medicine committed to a multisectoral engagement to promote sustainable sanitation in order to achieve human
                dignity and peaceful sustainable development<br><br>

            </p>
            <h6 class="text-font3">III. ENGINEERING RECOURCE CENTER</h6>
            <p>
                The Xavier University Engineering Resource Center manages, facilitates and operated the College of Engineering’s
                mission to spearhead sustainable development by providing pool of experts and facilities. It actively collaborated
                 with institutions for technological advancements and responds to the pressing engineering needs of communities.
                 It pursues topnotch and significant researches and innovation opportunities in next-generation technologies,
                 services, consultancies, businesses and social development programs. It serves as a venue for dialogue on the
                 application, the socioeconomic and ethical impacts of technology.<br><br>

            </p>
            <h6 class="text-font3">IV. ARRUPE EDUCATIONAL CENTER</h6>
            <p>
                The Arrupe Educational Center is the social development arm of the School of Education. It serves as the hub
                that will support the aspiration of the school to form teacher-leaders, with character who will engage and
                serve the literacy needs of the communities in Mindanao, ASEAN and the world..<br><br>

            </p>
