            <p>
                The Office of Student Affairs (OSA) exists to coordinate the non-academic support services
                for the students and facilitate a dynamic and exciting university life. It ensures an atmosphere
                conducive for study and the growth and development of the students.<br><br>


                Under OSA is the Student Activities and Leadership Development (SACDEV). It is directly involved
                in assisting students formulate effective organizations and in providing opportunities and services
                 that enrich students’ classroom experience and non-academic endeavors. It oversees the recognition
                 and management of over 70 student organizations composed of the Central Student Government, college
                 councils, co-curricular, organizations, extra-curricular organizations, and professional organizations
                 among others. It also works for the individual development of the students through training programs that
                 in accord with developing the students’ leadership and management skills.    <br><br>

                OSA also facilitates the following:<br><br>
                ORSEM (Orientation Seminar) for New Students<br>
                Xavier University Festival Days<br>
                Recognition of Student Organization and Student Leaders through Magis Awards<br>
                Release of Good Moral Certificates<br>
                Processing of University ID<br>
                Publication of the Student Handbook<br>
                Processing of Student Accident Insurance Claims<br>
                Implementation of the Prescribed Uniform, Student ID, and Dress Code<br>
                Posting and release of campus mail<br>
                Custodian of lost and found items  <br>
                Facilitation of campus tours for visiting institutions<br>
                Endorsement for use of University facilities and processing of classroom reservations<br>
                Approval of promotional materials on campus<br>
                Supervision of official class bell<br>
