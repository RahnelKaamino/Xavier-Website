        <h2 id="general" class="text-font2">Academics</h2>
        <div class="content" id="content">
            <br>
            <p class="text-font3">The Core Subjects define the Atenean Education as Xavier University aspires to build character in its students and mold them to become engaged citizens and effective leaders.</p>
                <p class="text-font3">The First-Year Formation Program (3 units) is a special curriculum that acquaints first-year students and transferees with Jesuit Education and the University’s mission of training persons who are competent, conscientious, and committed to serve God and society. Students, as part of their formation process, learn about Ignatian spirituality and participate in activities like prayer and reflection, social exposures, peer support, and group consultation to strengthen their faith, enhance their save personal well-being, and promote social justice.</p>
                <p class="text-font3">The government-mandated National Service Training Program (3 units) for sophomores intensifies the involvement of students in civic consciousness activities such as weekly community service.</p>

                <p class="text-font3">The Religious Studies (RS) subjects (12 units) give our students a strong foundation on their Catholic faith with courses that cover Scriptures and Old Testament, Christology, Church and Sacraments, Christian Morality and the Social Teachings of the Church.A parallel subject, the Muslim Religious Studies (MRS), is also offered for Muslim students.</p>
                
                <p class="text-font3">The Philosophy subjects (12 to 15 units) teach students the art of “liberal thinking.” While RS/MRS subjects strengthen one’s faith, the Philosophy subjects complement them by presenting different perceptions of reality and existences. Students take up courses on Logic, Philosophy of Man, Ethics, and Philosophy of Religion. The Liberal Arts subjects, such as communication subjects (English and Filipino with 6 units each), Math (at least 12 units), Sciences and Technology (at least 9 units), re-establish the “renaissance man,” a student who is knowledgeable of the world around him, both in the arts and technology.</p>
           
