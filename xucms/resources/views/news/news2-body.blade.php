            <p>
                Organized by the American Corner Cagayan de Oro of Xavier University, a free webinar "Information: expanding
                the conversation on how to promote responsible media reporting, ethical journalism, and digital citizenship to
                counter disinformation, was held this morning which adapted for the first time a hybrid platform where there were
                face-to-face participants and in Zoom as well. <br> <br>

                The very engaging 2-hour webinar had as Resource Speaker Ms Rechelle Ann Tolinero-Barraquias, currently the department
                chairperson of the Development Communication Department at Xavier University-Ateneo de Cagayan. She earned her master’s degree
                in Development Communication at the University of the Philippines Open University in 2016. She is an alumna of the
                United States’ International Visitor Leadership Program (IVLP) for Promoting Media Literacy through Education and of
                The Netherlands' Radio Radio Netherlands Training Centre for Media Campaigns for Development.<br> <br>

                As Ms Barraquias presented the types of information disorder – dis-information, mis-information and mal-information, the
                15 participants onsite and 25 in Zoom, were allowed to share their views and practiced judgment on some real samples of news
                both in mainstream and social media.<br> <br>

                “Your freedom of expression ends with the freedom of expression of other people. It it harms other people, then it’s
                not freedom at all” Ms Barraquias emphasized.<br> <br>

                She ended the webinar by sharing 5 ways to promote responsible reporting and digital citizenship.<br> <br>

