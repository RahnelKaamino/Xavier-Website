        <h2 id="general" class="text-font2">XU-CCS dean among new United Board Fellows, talks 21st-century learning in ‘Campus of the Future’</h2>
        <div class="content" id="content">
            <br>
            <img src="/assets/img/7.png" alt="" class="img-fluid">
            <p>
                “Learning environments today need to embrace the variety of places, ideas, and
                people that the modern world demands and reflect the flexibility of space, time,
                people, and technology,” shares Dr Meldie A Apag of XU, dean of the College of
                Computer Studies of Xavier University – Ateneo de Cagayan. <br> <br>

                Dean Apag was recently named as one of the fellows comprising the United Board’s 2022-2023 cohort.
                This program of the United Board for Christian Higher Education in Asia answers “the needs of
                leadership development for mid-career faculty and administrators from Asian colleges and universities.”
                “Having been chosen as United Board Fellows for the 2022-2023 cohort, we will undergo eight
                months of intensive online training that will be curated and delivered by the Singapore Management University,” shares Dean Apag. <br> <br>

                There will also be an optional two-week study trip either in or outside of one’s home country
                in Asia and a week-long in-person seminar in Singapore to conclude the one-year program.<br> <br>

                Through this program, the United Board aims to develop dynamic leaders committed to the advancement
                of whole-person education within their home institutions.
                Since 2002, nearly 300 Fellows from over 50 institutions in 11 countries and regions of Asia have
                been selected for this program.<br> <br>

                Bringing Xavier Ateneo to the 21st century
                Dean Apag is committed to helping in innovating the learning environment of Xavier Ateneo through
                the integration of information communication technologies (ICTs).
                “Effective learning environments do not limit themselves to time or space but comprise a variety
                of support systems that take into consideration the ways in which we learn best as well as the unique
                learning needs of each student,” she shares.<br> <br>

                “Our goal is always to create learning environments where educators and students can thrive.
                Technology can help seamlessly support these learning environments by building physical and
                digital arrangements.”<br> <br>

                When asked about the role of the College of Computer Studies as the university moves toward a
                21st-century learning setup, specifically for the Campus of the Future project, Dean Apag answers:<br> <br>

                “The College of Computer Studies may be able to lead the Campus of the Future with digital
                technology in the aspect of technology integration in the classrooms and the whole institution.
                Modern classrooms must use technology as a tool to stimulate curiosity and inspire students’
                desire to learn and teachers’ desire to teach. […] We may take part in the building of the Campus
                of the Future that takes a multimedia approach to learn core content, promote essential learning
                and innovation skills, build information, and even take responsibility for sustainability." <br>

